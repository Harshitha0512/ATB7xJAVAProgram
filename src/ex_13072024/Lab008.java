package ex_13072024;

public class Lab008 {
    public static void main(String[] args) {
        //Data types - Primitive & Non-Primitive
        //data_type variable_name = variable_value(literals)
        boolean you_married = true;
        boolean are_you_dog = false;
        System.out.println(you_married);
        System.out.println("i'm married");
        System.out.println(are_you_dog);

        byte b =100; //byte
        short s = 101; //short
        int age = 10; // integer

        System.out.println(b);
        System.out.println(s);
        System.out.println(age);

        char grade = 'A'; // character
        float pi = 3.142f; // float
        double d = 1111.889; // double

        System.out.println(grade);
        System.out.println(pi);
        System.out.println(d);
        //non-primitive data type
        String name = "Harshitha";
        System.out.println(name);

    }
}
