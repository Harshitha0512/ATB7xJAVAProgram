package ex_13072024;

public class Lab020 {
    public static void main(String[] args) {
        //Compound Assignment Operator += -= *= %= /=
        int a = 10;
        System.out.println(a+=10);
        System.out.println("value of a is " + a);
        System.out.println(a-=10);
        System.out.println("value of a is " + a);
        System.out.println(a/=10);
        System.out.println("value of a is " + a);
        System.out.println(a*=10);
        System.out.println("value of a is " + a);
        System.out.println(a%=10);
        System.out.println("value of a is " + a);

    }
}
